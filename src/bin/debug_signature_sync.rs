use base64::{engine::general_purpose::STANDARD as BASE64, Engine};
use hmac::{Hmac, Mac};
use serde_json::{Map, Value};
use sha2::Sha256;
use std::collections::BTreeMap;

type HmacSha256 = Hmac<Sha256>;

fn canonicalize(value: &Value) -> Value {
    match value {
        Value::Object(map) => {
            let btree_map: BTreeMap<String, Value> = map
                .iter()
                .map(|(k, v)| (k.clone(), canonicalize(v)))
                .collect();
            Value::Object(Map::from_iter(btree_map))
        }
        Value::Array(arr) => Value::Array(arr.iter().map(canonicalize).collect()),
        _ => value.clone(),
    }
}

fn sign(
    command: &str,
    timestamp: i64,
    nonce: &str,
    payload: &Value,
    key: &[u8],
) -> Result<String, Box<dyn std::error::Error>> {
    let canonical_payload = canonicalize(payload);
    let compact_payload = serde_json::to_string(&canonical_payload)?;
    let string_to_sign = format!("{command}|{timestamp}|{nonce}|{compact_payload}");

    println!("Rust test signing:");
    println!("  Command: {}", command);
    println!("  Timestamp: {}", timestamp);
    println!("  Nonce: {}", nonce);
    println!("  Canonical payload: {}", compact_payload);
    println!("  String-to-sign: {}", string_to_sign);

    let mut mac = HmacSha256::new_from_slice(key)?;
    mac.update(string_to_sign.as_bytes());
    let signature_bytes = mac.finalize().into_bytes();
    let signature = BASE64.encode(signature_bytes);

    println!("  Generated signature: {}", signature);
    Ok(signature)
}

fn main() -> Result<(), Box<dyn std::error::Error>> {
    println!("Testing signature generation with exact values from logs:");

    let key =
        BASE64.decode("dGVzdF9pcGNfa2V5XzEyMzQ1Njc4OTBhYmNkZWZnaGlqa2xtbm9wcXJzdHV2d3h5eg==")?;
    println!("Key (decoded): {}", String::from_utf8_lossy(&key));
    println!("Key length: {}", key.len());

    let command = "scan_summary";
    let timestamp = 1756473553;
    let nonce = "4nMXb0ZQDEFx+MUdJXgI1w==";
    let payload = serde_json::json!({});

    let signature = sign(command, timestamp, nonce, &payload, &key)?;

    println!("\nExpected from agent logs: zTJ+a1kYqWtj7QZUK9fy3KSTEBQLcMnh2JCnnlyjkDI=");
    println!("Generated by Rust: {}", signature);
    println!(
        "Match: {}",
        signature == "zTJ+a1kYqWtj7QZUK9fy3KSTEBQLcMnh2JCnnlyjkDI="
    );

    Ok(())
}
